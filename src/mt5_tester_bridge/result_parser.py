"""MT5 Strategy Tester Result Parser.

Parses XML/HTML report files generated by MT5's Strategy Tester.
Extracts trades, equity curve, drawdown, and performance metrics.
"""
import os
import re
import xml.etree.ElementTree as ET
from pathlib import Path
from typing import Optional

from loguru import logger


def parse_tester_results(run_dir: str) -> dict:
    """Parse all tester results from a run directory.

    Looks for XML and HTML report files, extracts trade data
    and performance metrics.

    Args:
        run_dir: Directory containing tester output files

    Returns:
        dict with parsed results: trades, equity_curve, stats
    """
    run_path = Path(run_dir)
    results = {
        "total_trades": 0,
        "winning_trades": 0,
        "losing_trades": 0,
        "total_pnl": 0.0,
        "profit_factor": 0.0,
        "max_drawdown": 0.0,
        "avg_slippage": 0.0,
        "avg_spread": 0.0,
        "sharpe_ratio": 0.0,
        "recovery_factor": 0.0,
        "trades": [],
        "equity_curve": [],
        "source": None,
    }

    # Try XML report first (most structured)
    xml_files = list(run_path.glob("*.xml"))
    if xml_files:
        xml_result = _parse_xml_report(xml_files[0])
        if xml_result:
            results.update(xml_result)
            results["source"] = str(xml_files[0])
            return results

    # Fallback to HTML report
    html_files = list(run_path.glob("*.htm")) + list(run_path.glob("*.html"))
    if html_files:
        html_result = _parse_html_report(html_files[0])
        if html_result:
            results.update(html_result)
            results["source"] = str(html_files[0])
            return results

    # Try results.json if tester produced one
    json_path = run_path / "results.json"
    if json_path.exists():
        import json
        try:
            with open(json_path) as f:
                data = json.load(f)
            if "parsed_results" in data:
                results.update(data["parsed_results"])
                results["source"] = str(json_path)
        except Exception as e:
            logger.warning(f"Failed to parse results.json: {e}")

    return results


def _parse_xml_report(xml_path: Path) -> Optional[dict]:
    """Parse an MT5 XML tester report.

    MT5 Strategy Tester can export detailed XML reports with
    trade history, equity data, and performance statistics.
    """
    try:
        tree = ET.parse(str(xml_path))
        root = tree.getroot()

        results = {
            "trades": [],
            "equity_curve": [],
            "total_trades": 0,
            "winning_trades": 0,
            "losing_trades": 0,
            "total_pnl": 0.0,
            "profit_factor": 0.0,
            "max_drawdown": 0.0,
        }

        # Try common MT5 XML structures
        # MT5 XML format varies by version, so we handle multiple schemas

        # Schema 1: <StrategyTesterReport> root
        if root.tag == "StrategyTesterReport" or root.find("StrategyTesterReport") is not None:
            report = root if root.tag == "StrategyTesterReport" else root.find("StrategyTesterReport")

            # General statistics
            for stat in report.iter():
                tag = stat.tag.lower()
                if "totaltrades" in tag or "total_trades" in tag:
                    try:
                        results["total_trades"] = int(float(stat.text))
                    except (ValueError, TypeError):
                        pass
                elif "profittrades" in tag or "winning" in tag:
                    try:
                        results["winning_trades"] = int(float(stat.text))
                    except (ValueError, TypeError):
                        pass
                elif "losstrades" in tag or "losing" in tag:
                    try:
                        results["losing_trades"] = int(float(stat.text))
                    except (ValueError, TypeError):
                        pass
                elif "profitfactor" in tag:
                    try:
                        results["profit_factor"] = float(stat.text)
                    except (ValueError, TypeError):
                        pass
                elif "maxdrawdown" in tag:
                    try:
                        results["max_drawdown"] = abs(float(stat.text))
                    except (ValueError, TypeError):
                        pass
                elif "totalnetprofit" in tag or "total_pnl" in tag:
                    try:
                        results["total_pnl"] = float(stat.text)
                    except (ValueError, TypeError):
                        pass

            # Trade list
            for trade_elem in report.iter("Trade"):
                trade = _parse_trade_element(trade_elem)
                if trade:
                    results["trades"].append(trade)

            # Equity curve
            for eq_elem in report.iter("Equity"):
                try:
                    results["equity_curve"].append({
                        "time": eq_elem.get("time", ""),
                        "value": float(eq_elem.get("value", 0)),
                    })
                except (ValueError, TypeError):
                    pass

        # Schema 2: Generic <trades> and <statistics> elements
        trades_elem = root.find("trades") or root.find("Trades")
        if trades_elem is not None:
            for trade_elem in trades_elem:
                trade = _parse_trade_element(trade_elem)
                if trade:
                    results["trades"].append(trade)

        stats_elem = root.find("statistics") or root.find("Statistics")
        if stats_elem is not None:
            results.update(_parse_statistics_element(stats_elem))

        # Derive statistics from trades if not in XML
        if results["trades"] and results["total_trades"] == 0:
            _derive_statistics(results)

        return results

    except ET.ParseError as e:
        logger.error(f"XML parse error in {xml_path}: {e}")
        return None
    except Exception as e:
        logger.error(f"Error parsing XML report {xml_path}: {e}")
        return None


def _parse_trade_element(elem: ET.Element) -> Optional[dict]:
    """Parse a single trade element from XML."""
    trade = {}
    for attr in ["time", "type", "symbol", "lots", "price", "sl", "tp",
                  "close_price", "profit", "commission", "swap", "duration"]:
        val = elem.get(attr) or elem.findtext(attr)
        if val is not None:
            try:
                trade[attr] = float(val) if attr not in ("time", "type", "symbol") else val
            except (ValueError, TypeError):
                trade[attr] = val
    return trade if trade else None


def _parse_statistics_element(elem: ET.Element) -> dict:
    """Parse a statistics element from XML."""
    stats = {}
    for child in elem:
        key = child.tag.lower().replace(" ", "_")
        try:
            stats[key] = float(child.text)
        except (ValueError, TypeError):
            stats[key] = child.text
    return stats


def _parse_html_report(html_path: Path) -> Optional[dict]:
    """Parse an MT5 HTML tester report.

    MT5 generates HTML reports with tables containing trade data
    and performance metrics. This uses regex-based extraction
    since HTML structure varies between MT5 versions.
    """
    try:
        with open(html_path, "r", encoding="utf-8", errors="replace") as f:
            content = f.read()

        results = {
            "trades": [],
            "equity_curve": [],
            "total_trades": 0,
            "winning_trades": 0,
            "losing_trades": 0,
            "total_pnl": 0.0,
            "profit_factor": 0.0,
            "max_drawdown": 0.0,
        }

        # Extract key metrics from HTML tables
        # MT5 HTML format uses <td> cells with labeled values

        # Total trades
        m = re.search(r"Total Trades[:\s]*</td>\s*<td[^>]*>(\d+)", content, re.I)
        if m:
            results["total_trades"] = int(m.group(1))

        # Profit Factor
        m = re.search(r"Profit Factor[:\s]*</td>\s*<td[^>]*>([\d.]+)", content, re.I)
        if m:
            try:
                results["profit_factor"] = float(m.group(1))
            except ValueError:
                pass

        # Max Drawdown
        m = re.search(r"Max Drawdown[:\s]*</td>\s*<td[^>]*>([\d.]+)", content, re.I)
        if m:
            try:
                results["max_drawdown"] = abs(float(m.group(1)))
            except ValueError:
                pass

        # Net Profit
        m = re.search(r"Total Net Profit[:\s]*</td>\s*<td[^>]*>[\s]*([\d.-]+)", content, re.I)
        if m:
            try:
                results["total_pnl"] = float(m.group(1))
            except ValueError:
                pass

        # Extract trade rows from HTML tables
        # Look for patterns like: <tr>...<td>time</td><td>BUY/SELL</td><td>symbol</td>...
        trade_pattern = re.compile(
            r"<tr[^>]*>\s*"
            r"<td[^>]*>([^<]+)</td>\s*"   # time
            r"<td[^>]*>([^<]+)</td>\s*"    # type (buy/sell)
            r"<td[^>]*>([^<]+)</td>\s*"    # symbol
            r"<td[^>]*>([\d.]+)</td>\s*"   # lots
            r"<td[^>]*>([\d.]+)</td>\s*"   # price
            r"(?:<td[^>]*>([\d.]+)</td>\s*)?"  # SL (optional)
            r"(?:<td[^>]*>([\d.]+)</td>\s*)?"  # TP (optional)
            r"<td[^>]*>([\d.]+)</td>\s*"   # close price
            r"<td[^>]*>([\d.-]+)</td>",     # profit
            re.I
        )

        for match in trade_pattern.finditer(content):
            try:
                trade = {
                    "time": match.group(1).strip(),
                    "type": match.group(2).strip(),
                    "symbol": match.group(3).strip(),
                    "lots": float(match.group(4)),
                    "price": float(match.group(5)),
                    "close_price": float(match.group(8)),
                    "profit": float(match.group(9)),
                }
                results["trades"].append(trade)
            except (ValueError, IndexError):
                continue

        # Derive statistics from parsed trades if needed
        if results["trades"]:
            _derive_statistics(results)

        return results

    except Exception as e:
        logger.error(f"Error parsing HTML report {html_path}: {e}")
        return None


def _derive_statistics(results: dict) -> None:
    """Derive statistics from parsed trade data when XML lacks summary stats."""
    trades = results.get("trades", [])
    if not trades:
        return

    results["total_trades"] = len(trades)
    results["winning_trades"] = sum(1 for t in trades if t.get("profit", 0) > 0)
    results["losing_trades"] = sum(1 for t in trades if t.get("profit", 0) < 0)
    results["total_pnl"] = sum(t.get("profit", 0) for t in trades)

    gross_profit = sum(t["profit"] for t in trades if t.get("profit", 0) > 0)
    gross_loss = abs(sum(t["profit"] for t in trades if t.get("profit", 0) < 0))
    results["profit_factor"] = gross_profit / gross_loss if gross_loss > 0 else float("inf")

    # Compute drawdown from equity curve if available
    if not results.get("equity_curve"):
        # Build equity curve from trades
        balance = 10000  # default starting balance
        curve = [{"time": "start", "value": balance}]
        for t in trades:
            balance += t.get("profit", 0)
            curve.append({"time": t.get("time", ""), "value": balance})
        results["equity_curve"] = curve

        # Max drawdown
        peak = curve[0]["value"]
        max_dd = 0.0
        for point in curve:
            peak = max(peak, point["value"])
            dd = peak - point["value"]
            max_dd = max(max_dd, dd)
        results["max_drawdown"] = max_dd


def parse_tester_xml(xml_path: str) -> dict:
    """Parse a specific XML report file.

    Convenience wrapper for _parse_xml_report.
    """
    return _parse_xml_report(Path(xml_path)) or {}


def parse_tester_html(html_path: str) -> dict:
    """Parse a specific HTML report file.

    Convenience wrapper for _parse_html_report.
    """
    return _parse_html_report(Path(html_path)) or {}